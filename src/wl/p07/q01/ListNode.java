package wl.p07.q01;

// A node in the linked list consists of integer 
// data item and a reference to next node 
// in a singly-linked list

public class ListNode {		
	int data;				
	ListNode next;		 	
	
	// Constructor	
	public ListNode (int value) {		
		data = value;		
		next = null;	
	}
}
